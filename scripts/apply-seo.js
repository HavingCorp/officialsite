function main() {
  const cfg = readConfig();
  console.log('apply-seo' + (checkOnly ? ' (check only)' : '') + ' — origin ' + cfg.site.origin);
  console.log('-'.repeat(60));
  let changed = 0, missing = 0;
  cfg.pages.forEach(function (p) {
    const r = processPage(cfg, p);
    if (r.status === 'updated' || r.status === 'would-change') changed++;
    if (r.status === 'missing' || r.status === 'no-head') missing++;
    console.log('  ' + r.status.padEnd(13) + r.file);
  });
  (cfg.noindexPages || []).forEach(function (f) {
    const r = processNoindex(cfg, f);
    if (r.status === 'noindexed' || r.status === 'would-change') changed++;
    console.log('  ' + r.status.padEnd(13) + f);
  });
  console.log('-'.repeat(60));
  console.log(cfg.pages.length + ' pages, ' + changed + ' changed, ' + missing + ' problems');
  if (missing > 0) process.exitCode = 1;
}

main();
